const express = require('express');
const auth = require('../../middlewares/auth');
const validate = require('../../middlewares/validate');
const { taskValidation } = require('../../validations');
const { taskController } = require('../../controllers');

const router = express.Router();

router.route('/createTask').post(auth(), taskController.createTask);
router.route('/listTask').get(auth(), validate(taskValidation.listTask), taskController.listTask);
router.route('/completedTask').get(auth(), validate(taskValidation.completedTask), taskController.completedTask);

module.exports = router;

/**
 * @swagger
 * tags:
 *   name: Tasks
 *   description: Tasks management and retrieval
 */

/**
 * @swagger
 * /tasks/createTask:
 *   post:
 *     summary: Create a task
 *     description: Authenticated user can create a task.
 *     tags: [Tasks]
 *     security:
 *       - bearerAuth: []
 *     responses:
 *       "200":
 *         description: OK
 *         content:
 *           application/json:
 *             schema:
 *               type: object
 *               properties:
 *                 results:
 *                   type: array
 *                   items:
 *                     $ref: '#/components/schemas/Task'
 *                 page:
 *                   type: integer
 *                   example: 1
 *                 limit:
 *                   type: integer
 *                   example: 10
 *                 totalPages:
 *                   type: integer
 *                   example: 1
 *                 totalResults:
 *                   type: integer
 *                   example: 1
 *       "401":
 *         $ref: '#/components/responses/Unauthorized'
 *       "403":
 *         $ref: '#/components/responses/Forbidden'
 */

